public class Variables {
  public static void main(String[] args) {
    int a = 5;               
    float b = 5.99f;    
    char c = 'M';         
    boolean d = true;       
    String e = "Hello";        
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }
}
